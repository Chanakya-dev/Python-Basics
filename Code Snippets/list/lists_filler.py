# Lists

# Adding to a List
fruits = ["apple", "banana"]
fruits.____("orange")
print(fruits)  # Should print: ['apple', 'banana', 'orange']

# Removing from a List
fruits = ["apple", "banana", "cherry"]
fruits.____("banana")
print(fruits)  # Should print: ['apple', 'cherry']

#  Sorting a List
numbers = [9, 3, 5, 2]
numbers.____()
print(numbers)  # Should print: [2, 3, 5, 9]
