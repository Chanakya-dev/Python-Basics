x = 12
y = 8

# Addition
result_add = x + y
print("Addition:", result_add)  # Output: 20

# Subtraction
result_sub = x - y
print("Subtraction:", result_sub)  # Output: 4

# Multiplication
result_mul = x * y
print("Multiplication:", result_mul)  # Output: 96

# Division
result_div = x / y
print("Division:", result_div)  # Output: 1.5

# Modulus
result_mod = x % y
print("Remainder:", result_mod)  # Output: 4

# Floor Division
result_floor = x // y
print("Floor Division:", result_floor)  # Output: 1

# Exponentiation
result_exp = x ** y
print("Exponentiation:", result_exp)  # Output: 110075314176
