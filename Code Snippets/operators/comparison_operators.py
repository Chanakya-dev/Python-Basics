x = 15
y = 10

# Equal To
result_eq = x == y
print("Equal To:", result_eq)  # Output: False

# Not Equal To
result_neq = x != y
print("Not Equal To:", result_neq)  # Output: True

# Greater Than
result_gt = x > y
print("Greater Than:", result_gt)  # Output: True

# Less Than
result_lt = x < y
print("Less Than:", result_lt)  # Output: False

# Greater Than or Equal To
result_ge = x >= y
print("Greater Than or Equal To:", result_ge)  # Output: True

# Less Than or Equal To
result_le = x <= y
print("Less Than or Equal To:", result_le)  # Output: False
