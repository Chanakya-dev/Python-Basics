x = 10

# Add and Assign
x += 5
print("Add and Assign:", x)  # Output: 15

# Subtract and Assign
x -= 3
print("Subtract and Assign:", x)  # Output: 12

# Multiply and Assign
x *= 2
print("Multiply and Assign:", x)  # Output: 24

# Divide and Assign
x /= 4
print("Divide and Assign:", x)  # Output: 6.0
